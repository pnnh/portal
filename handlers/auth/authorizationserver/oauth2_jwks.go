func JwksEndpoint(gctx *gin.Context) {
	jwkString, err := helpers.GetJwk()
	if err != nil {
		gctx.AbortWithError(500, err)
		return
	}
	gctx.Header("Content-Type", "application/json; charset=utf-8")
	resp := fmt.Sprintf(`{"keys":[%s]}`, jwkString)

	gctx.String(200, resp)
}
